#program to demonstrate what math functions you can use in python

a = 10
b = 3

print('a =', a)
print('b =', b)

print('a + b =', a + b)
print('a - b =', a - b)
print('a * b =', a * b)
print('a / b =', a / b)
print('a // b =', a // b)
print('a % b =', a % b)
print('a ** b =', a ** b)

print('\n')

print(f'+ means addition', '- means subtraction',
      f'* means multiplication', '/ means division', 
      f'// means floor division', '** means exponentiation', 
      f'% means modulus', sep='\n')