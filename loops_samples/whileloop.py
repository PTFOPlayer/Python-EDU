#program demonstrates while loop

def main():
    #initialize variables
    count = 0
    #print a message
    print("This program counts from 0 to 9 using a while loop")
    #while loop
    while count < 10:
        #print the count
        print(count)
        #increment the count
        count = count + 1
    #print a message
    print("Done")
    
if __name__ == "__main__":
    main()